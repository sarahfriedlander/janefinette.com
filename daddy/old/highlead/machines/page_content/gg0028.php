    <td>
The GG0028 zig zag machine utilizes a large capacity rotating hook, a
parallel vertical needle bar motion for strong striking power and a
semi automatic lubrication system to produce attractive stitching with
a long service life.  It has the capability to be used as a zig zag
machine, a straight stitch machine, or a decorative stitch machine.
It features convenient reverse feeding and easy adjustment of zig zag
width (up to 10 mm) and zig zag length (up to 5 mm).  It is used on
such medium heavy weight products as shoes, soft cases and bags,
slippers, canvas goods, leather products, furniture and similar items.

