    <td>Model GK500-4 is Highlead's newest style 3 needle 5 thread differential feed flat bed top and bottom cover stitch machine.  The machine is very smooth and quiet running being automatically lubricated by a fully sealed gear pump that circulates oil throughout the machine and its moving parts. Needle bearings and timing belts are used in critical driving positions. Easy adjustment of the bottom differential feeding system, an easy to use knob type stitch regulator and easy machine threading are but a few of the machines many features. It is used in the production of a variety of knit goods such as underwear, sportswear, T shirts and polo shirts and is capable of covering seams as well as being used as a decorative stitch on the above mentioned and similar articles.</td>
