    <td>  Model GK600-4 is Highlead's newest style 3 needle 5 thread differential feed cylinder bed top and bottom cover stitch machine. Like flat bed model GK500-4 the machine is very smooth and quiet running being automatically lubricated by a fully sealed gear pump that circulates oil throughout the machine and its moving parts. Needle bearings and timing belts are used in critical driving positions. A silicon oil thread lubricator, needle cooler, and
      knob type stitch regulator are but a few of the standard features. The machine is used extensively in the construction of knit garments and is best suited for such operations as bottom hemming, collar binding, elastic waistband attaching and similar operations with small areas to join where a cylinder bed machine is better suited to facilitate material handling and ease of access.</td>
