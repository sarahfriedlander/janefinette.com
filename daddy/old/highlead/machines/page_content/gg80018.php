    <td>
    	Model GG80018 is a long (30 inches) and high arm heavy duty zig zag machine.  The machine comes standard with a special fitted Racing puller to assist with material feeding.  Interchangeable cams allow you to work as a regular (2 step) zig zag,
		a 2 stitch (3 step) zig zag or a 3 stitch (4 step) zig zag according to requirements.  With a 2 inch needle bar stroke and a powerful feeding system it can produce a zig zag with a maximum width of 1/2 inch and a long stitch length with a maximum of  2 1/2 stitches per inch.  It is useful in the construction of sails, tents, aircraft interiors, and large and special articles made of geotextiles and geomembranes.

