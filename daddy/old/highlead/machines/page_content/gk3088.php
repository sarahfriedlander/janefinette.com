  <tr valign="top">
    <td>   A high speed two or three needle feed off the arm chainstitch machine that
      adopts link take up and lower loopers to produce two or three parallel rows
      of chainlock stitching. Ball bearings are used in all critical support areas and
      a drive belt is used to drive the top arm shaft and lower shaft. The
      loopers can be adjusted independently of one another to maximize
      stitching quality when using different fabrics and threads. The
      machine is best suited for use on light to medium heavy weight
      material (GK3088-2) or heavy weight material such as denim (GK3088-3B which is equipped with puller)

      and is used in lap seaming to join pant legs, shirt under
      arms and on other tubular like sewing operations. It is also well suited

      to sew tape inside of T-shirts and knit shirts.

	
	
	
