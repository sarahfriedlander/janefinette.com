    <td><p style="margin-bottom: 13px;">    This standard 25" long arm machine has a wider working space for harder to
handle items.  The bobbin winder on the top
 middle of the head makes for easy operation.  Available as a single or twin
needle machine it incorporates a large vertical
 axis hook (or hooks) which provide excellent stitch quality while allowing
smooth and quiet operation with a long service life.  A longer version of
models GC20618-1 and GC20618-2 it offers many of the same features in a
longer machine and now comes in a 40" version (models GC20698-1L and
GC20698-2L - an industry first) as a special order and can be used to sew
tents, awnings, car and truck covers, hot air balloons, sails and other
large work pieces.  Models GC20698-1-D and GC20698-2-D are available as
standard 25" machines with undertrimming and added functions.
