<h1>About</h1>
<p>For 75 years Friedlander Sewing Machine Co., Inc. has been buying and selling used and new machinery for the sewn products industry. The current president, Joseph, is the third generation of Friedlander’s to manage the daily operations of the business.
</p>
<p>
Originally we serviced and rented machinery and auxiliary equipment to the ever expanding New York City garment trade. As manufacturing was relocating to the southern part of the United States in the 1960’s and 1970’s, Sid Friedlander, the second generation owner, expanded the business outside of its New York home base. Sid’s travels were so extensive that he was often called the “Henry Kissinger” of the sewing machine industry.
</p>
<p>
After graduating from Northwestern University in 1974 with an engineering degree, Joe Friedlander, Sid's son, joined the business on a full time basis. Since then the company has expanded into the world market to become one of the largest buyers and sellers of used and new machinery. Joe’s honest approach and knowledge of both product and market value have made him a well respected resource for many others in the industry.
</p>
<p>
We have recently embarked on our newest endeavor as the United States agency for Chinese made Highlead brand sewing machines. These high quality machines, with a concentration on heavy duty stitching for the leather, upholstery and canvas trades, are sold on a wholesale basis to local dealers and distributors. With the addition of the Highlead line, Friedlander Sewing Machine is now the most complete source for all of your sewing machine needs.</p>
